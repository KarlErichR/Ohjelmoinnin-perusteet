

def add(a: float, b: float)->float:
    result = a + b
    return float(result)

def  substract(a: float, b: float):
    result = a - b
    return float(result)

def multiply(a: float, b: float):
    result = a * b
    return float(result)

def divide(a: float, b: float):
    result = a / b
    return float(result)